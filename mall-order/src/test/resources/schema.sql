    order_sn        VARCHAR(64)   NULL,
    order_id        BIGINT        NULL,
    sku_id          BIGINT        NULL,
    member_id       BIGINT        NULL,
    return_type     INT           NULL,
    reason          VARCHAR(255)  NULL,
    description     VARCHAR(500)  NULL,
