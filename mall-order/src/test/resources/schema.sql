CREATE TABLE IF NOT EXISTS oms_refund_info (
    id              BIGINT        NOT NULL,
    order_return_id BIGINT        NULL,
    order_sn        VARCHAR(64)   NULL,
    refund_sn       VARCHAR(64)   NULL,
    refund_amount   DECIMAL(10,2) NULL,
    refund_status   INT           NOT NULL DEFAULT 0,
    refund_channel  VARCHAR(32)   NULL,
    refund_content  VARCHAR(500)  NULL,
    create_time     DATETIME      NOT NULL DEFAULT CURRENT_TIMESTAMP,
    update_time     DATETIME      NOT NULL DEFAULT CURRENT_TIMESTAMP,
    refund          DECIMAL(10,2) NULL,
    PRIMARY KEY (id)
);

